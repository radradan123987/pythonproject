'''

관계 연산자
    2개 항을 비교하여 그 결과를 논리(bool) 자료형으로 반환
    ex) >, >=, <, <=, ==, !=

Ctrl + D 줄복사
'''
a = 15
print('{} > 10 : {}'.format(a, a > 10))
print('{} < 10 : {}'.format(a, a < 10))
print('{} >= 10 : {}'.format(a, a >= 10))
print('{} <= 10 : {}'.format(a, a <= 10))
print('{} == 10 : {}'.format(a, a == 10))
print('{} != 10 : {}'.format(a, a != 10))





